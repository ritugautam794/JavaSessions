package Basics;

public class LoopsConcepts {

	public static void main(String[] args) {
		// 1. while loop
		
		int i= 1;                 //Initialization
		while(i<=10) {             // Condition
			System.out.println(i); 
			i=i+1;   //or i++     //Increment
		}
		
		
		//2. for loop
		
		for(int x=10;x>=1;x--) {
			System.out.println(x);
		}
		
		

	}

}

package InterviewQuestionsTricky;

public class IntegerCaching {

	public static void main(String[] args) {
		// integer caching range, -128 to 127
		
		int a = 100;
		int b = 100;
		
		if(a==b) {
			System.out.println("equal");
		}else {
			System.out.println("not equal");
		}
		
		int x = 200;
		int y = 200;
		if(x==y) {
			System.out.println("equal");
		}else {
			System.out.println("not equal");
		}
		
		
		Integer x1 = 200;
		Integer y1 = 200;
		if(x1==y1) {
			System.out.println("equal");
		}else {
			System.out.println("not equal");
		}
		
		Integer x2 = 100;
		Integer y2 = 100;
		if(x2==y2) {
			System.out.println("equal");
		}else {
			System.out.println("not equal");
		}
		
		

	}

}
